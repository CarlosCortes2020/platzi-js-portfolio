var numero = 1;

switch (numero) {
    case 1:
        console.log("Son un 1")        
        break;
    case 10:
        console.log("Son un 10")        
        break;
    case 100:
        console.log("Son un 100")        
        break;        
    default:
        console.log("No soy nada")        
        break;
}